        function get_sis_file_name( $file_name, $width, $height, $crop ){
            $file_name_only = pathinfo( $file_name, PATHINFO_FILENAME );
            $file_extension = strtolower( pathinfo( $file_name, PATHINFO_EXTENSION ) );
            $crop_extension = '';

            if( $crop === true || $crop === 1 ){
                $crop_extension = '-c';
            } elseif( $crop === 'face' ){
                $crop_extension = '-fd';
            } elseif( is_array( $crop ) ){
                if( isset($crop[0]) && is_numeric( $crop[0] ) ){
                    $crop_extension = '-f' . round( floatval( $crop[0] ) * 100 ) . '_' . round( floatval( $crop[1] ) * 100 );
                } else {
                    $crop_extension = '-' . implode( '', array_map( function( $position ){
                        if (is_array($position) && isset($position[0])) {
                            return $position[0];
                        }
                        return ''; // Return an empty string if $position is not valid
                    }, $crop ) );
                }
            }

            $filename =  $file_name_only . '-' . intval( $width ) . 'x' . intval( $height ) . $crop_extension . '.' . $file_extension;
            return $filename;
        }

