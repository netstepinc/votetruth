                        //Remove the upload URL from the file path
                        $metadata['file'] = str_replace(wp_upload_dir()['baseurl'], '', $file_path);
                        //Remove the site URL from the file path/assets to deal with previous configuration
                        $metadata['file'] = str_replace(home_url('/assets'), '', $metadata['file']);
                    }
