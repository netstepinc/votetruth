        /* Detect duplicate images generated by WordPress */
        function sis_find_wp_generated_clones(){
            echo '<div style="background-color: #f0f0f0; padding: 10px; border-radius: 5px;"><h2>Identifying Cloned Images Generated by WordPress Plugins</h2>';
            $i=0;
            $d=0;
            $clones = array();
            global $wpdb;
			$upload_dir = wp_upload_dir();

            $attachments = get_posts(array(
                'post_type' => 'attachment',
                'posts_per_page' => -1,
                'post_status' => 'any',
                'meta_key' => '_wp_attachment_metadata',
            ));
            foreach($attachments as $attachment){
                $i++;

                // Get attachment metadata
                $metadata = wp_get_attachment_metadata($attachment->ID);
                if(!$metadata) return false;

                // Get path to original image
                $raw_name = $metadata['file'];
                //Get file name without extension or directory prefix
                $file_name = basename($raw_name);
                $file_name_no_ext = pathinfo($file_name, PATHINFO_FILENAME);

                $original_file = $upload_dir['basedir'] . '/' . $raw_name;
                $path_parts = pathinfo($original_file);

                echo '<ul style="border-bottom: 1px solid #666; padding-bottom: 10px;">[' . $attachment->ID . '] ' . $raw_name . ' | <b>' . $file_name_no_ext . '</b> | ' . $original_file;

                //Detect registered duplicates: Is this image file name a sub string of another image file name? Query where guid like %$file_name_no_ext%
                $query = "SELECT * FROM {$wpdb->prefix}posts WHERE post_type = 'attachment' AND post_status = 'inherit' AND post_mime_type IN ('image/jpeg', 'image/png', 'image/webp') AND LOWER(guid) LIKE '%" . strtolower($file_name_no_ext) . "%' AND guid NOT LIKE '%" . strtolower($file_name) . "%'";
                $results = $wpdb->get_results($query);
                foreach($results as $result){
                    $d++;
                    //Determine if this file is attached to a post
                    // Check if image is used as featured image
                    $featured_image_post = $wpdb->get_var($wpdb->prepare(
                        "SELECT post_id FROM {$wpdb->prefix}postmeta WHERE meta_key = '_thumbnail_id' AND meta_value = %d",
                        $result->ID
                    ));

                    // Check if image is used in post content
                    $content_usage = $wpdb->get_var($wpdb->prepare(
                        "SELECT ID FROM {$wpdb->prefix}posts WHERE post_content LIKE %s",
                        '%' . $wpdb->esc_like($result->guid) . '%'
                    ));

                    if ($featured_image_post) {
                        $post = get_post($featured_image_post);
                        $is_used = '<span style="color:#008000;">Featured image for: ' . $post->post_title . '</span>';
                    } else if ($content_usage) {
                        $post = get_post($content_usage); 
                        $is_used = '<span style="color:#0000ff;">Used in content: ' . $post->post_title . '</span>';
                    } else {
                        $is_used = '<span style="color:#ff0000;">Not in use</span>';
                    }

                    $clones[] = $result->ID . '|' . $result->guid;
                    echo '<li style="padding-left: 20px; font-weight:bold;">[' . $result->ID . '] CLONE: ' . str_replace($file_name_no_ext,$file_name_no_ext.'<span style="color:#ff0000;">',$result->guid) . '</span> ' . $is_used . '</li>';
                }

                echo '</ul>';
            }
            echo '<h3>Identified <strong>' . $d . '</strong> clones for <strong>' . $i . '</strong> images</h3>';
            echo '<textarea style="width:100%; height:200px;">' . implode("\n", $clones) . '</textarea>';
            echo '</div>';
        }


