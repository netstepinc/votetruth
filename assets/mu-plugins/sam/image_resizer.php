        function init_directories() {
            // Only initialize for current site
            $current_blog_id = get_current_blog_id();
            
            // Skip if we've already initialized for this site
            if (!empty($this->sis_dir) && strpos($this->sis_dir, '/sites/' . $current_blog_id . '/') !== false) {
                return;
            }
            
            $this->sis_dir = apply_filters('sis_dir_path', $this->get_sis_dir());
            $this->check_sis_dir();
        }

        function blog_switched(){
            // Only process if we're actually switching blogs and dealing with media
            $new_blog_id = get_current_blog_id();
            if (!empty($this->sis_dir) && strpos($this->sis_dir, '/sites/' . $new_blog_id . '/') !== false) {
                // Already on the correct blog, no need to switch
                return;
            }
            
            // Clear WordPress upload dir cache
            wp_get_upload_dir(true);
            // Reset the sis_dir
            $this->sis_dir = '';
            $this->init_directories();
        }

        function get_sis_dir($path = ''){
            if(empty($this->sis_dir)){
                $wp_upload_dir = wp_upload_dir();
                $base_dir = $wp_upload_dir['basedir'];
                
                // Debug logging
                //error_log("Attempting to access base_dir: " . $base_dir);
                //error_log("Current blog ID: " . get_current_blog_id());
                //error_log("Upload dir structure: " . print_r($wp_upload_dir, true));
                
                // Create img directory path
                $img_dir = $base_dir . DIRECTORY_SEPARATOR . 'img';
                //error_log("Attempting to create/access img_dir: " . $img_dir);
                
                // Check/create parent directories with proper permissions
                if(!is_dir($base_dir)){
                    //error_log("Base directory doesn't exist: " . $base_dir);
                    if(!wp_mkdir_p($base_dir)){
                        //error_log("Failed to create base upload directory: " . $base_dir);
                        return false;
                    }
                    chmod($base_dir, 0755);
                }

                // Return full path
                $final_path = $img_dir . ($path !== '' ? DIRECTORY_SEPARATOR . $path : '');
                //error_log("Final path being returned: " . $final_path);
                return $final_path;
            } else {
                return $this->sis_dir . ($path !== '' ? DIRECTORY_SEPARATOR . $path : '');
            }
        }

        function check_sis_dir(){
            if(!empty($this->sis_dir) && !is_dir($this->sis_dir)){
                // Check parent directory permissions first
                $parent_dir = dirname($this->sis_dir);
                if(!is_dir($parent_dir)){
                    if(!wp_mkdir_p($parent_dir)){
                        //error_log("Failed to create parent directory: " . $parent_dir);
                        return false;
                    }
                    chmod($parent_dir, 0755);
                }
                
                if(!is_writable($parent_dir)){
                    //error_log("Parent directory not writable: " . $parent_dir);
                    return false;
                }
                
                // Create directory with explicit permissions
                if(!wp_mkdir_p($this->sis_dir)){
                    //error_log("Failed to create JIS directory: " . $this->sis_dir);
                    return false;
                }
                chmod($this->sis_dir, 0755);
            }
            return true;
        }

        function sis_dir_writable(){
            return is_dir( $this->sis_dir ) && wp_is_writable( $this->sis_dir );
        }

        function delete_all_sis_images(){
            if( ! function_exists( 'WP_Filesystem' ) ) return false;
            WP_Filesystem();
            global $wp_filesystem;
            if( $wp_filesystem->rmdir( $this->get_sis_dir(), true ) ){

                $this->check_sis_dir();
                return true;
            }

            return false;
        }

