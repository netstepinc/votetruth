// Flatten the full constitution array to key => label (handles both string and array entries).
function fi_constitution_links_flat(): array {
	$flattened = [];
	foreach (FI_CONSTITUTION_LINKS as $key => $value) {
		if (is_array($value)) {
			$flattened[$key] = $value['title'];
			foreach ($value['sections'] as $skey => $slabel) {
				$flattened[$skey] = $slabel;
			}
		} else {
			$flattened[$key] = $value;
		}
	}
	return $flattened;
}

// Select field options — indented sections under parent label (handles both entry formats).
function fi_constitution_link_options(): array {
	$options = [];
	foreach (FI_CONSTITUTION_LINKS as $key => $value) {
		if (is_array($value)) {
			$options[$key] = $value['title'];
			foreach ($value['sections'] as $skey => $slabel) {
				$options[$skey] = '— ' . $slabel;
			}
		} else {
			$options[$key] = $value;
		}
	}
	return $options;
}

//Build the link based on the key
function fi_constitution_link_get($key){
    return home_url('/constitution/').'#'.$key;
}

//Reformat the key into citation abbreviation format: Art-I_Sec-8 = Art. I, Sec. 8 
function fi_constitution_citation($key){
    $parts = explode('_', $key);
    $article = $parts[0];
    $article = str_replace('-', '. ', $article);

    $section = '';
    if(isset($parts[1])){
        $section = $parts[1];
        $section = str_replace('-', '. ', $section);
    }

    return $article.' '.$section;
}